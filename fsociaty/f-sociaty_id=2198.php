MONO是成立于日本东京的4人乐队，最初组建于1999年。他们的音乐独树一帜，将细腻的管弦乐、迷幻的shoegaze噪音完美融合于传统四大件构造的器乐声场中。英国NME 杂志曾用“诸神的美誉”一词高度评价这支来自东方的器乐摇滚乐队。

每年多达150场的巡回演出，足迹遍布全球50多个国家，MONO被誉为日本最具国际影响力的乐团。而在后摇迷的眼中，这支以现场表现力见长的乐队无疑是他们心中的最佳现场乐队。MONO曾在纽约、东京、伦敦和墨尔本与23人的交响乐团合作跨界音乐会，这一里程碑式的事件也让MONO的乐队生涯迈上新的台阶。

今年，MONO刚刚在芝加哥的录音室里完成了最新专辑《Requiem For Hell》的录音工作，将于今年10月正式发行。


“Requiem For Hell” 意为 “地狱安魂曲”，MONO的灵魂人物TAKA亲自讲述了整张专辑的构思：

《Requiem For Hell》的创作灵感大多是在2015年产生的。在忙碌的创作和巡演之余，我们偶然读到了但丁的《神曲》，并从中获得了很多共鸣。《神曲》讲述诗人但丁游历了地狱、炼狱、天堂，最终回归现实世界的故事，而MONO的这张专辑沿用了这一主题。专辑的主视觉以及封面，同样来自于艺术家 Gustave Dóre为《神曲》创作的系列插画作品。

我们希望借由这张专辑， 让大家感受生命中的万象——生与死，灵魂与肉体，希望与绝望、光明和黑暗，尊严和荣耀，以及新生。" ——TAKA

《Requiem For Hell》收录了5首歌。与上一张专辑追溯MONO最初的声音不同，新专辑中MONO再次引入了弦乐，在声音的张力、纵深以及情绪色彩上都更进一步。专辑预计将在10月14日全球发行，唱片公司Temporary Residence（北美）， Pelagic Records（英国&欧洲）以及Magniph/Hostess（日本&亚太）将同步上线。

乐队也将在10月13日开启他们的新专辑中国巡演，令人倍感期待的是，此次巡演的首站北京站，MONO 将会在13号和14号在愚公移山为大家分别呈现两场曲目和主题完全不同的特别现场。
